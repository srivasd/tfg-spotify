
# range (init, final)
for x in range(3, 10):
    print(x)

# tuples
names = ('Manuel', 'Fernando', 'Yolanda', 'Mario')
for x in names:
    print(x)
else:
    print('No names left')

# dictionaries
dictionary = {1: 'Manuel', 2: 'Fernando', 3: 'Yolanda', 4: 'Mario'}
for key, value in dictionary.items():
    print(key, ':', value)



