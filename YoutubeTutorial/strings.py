# Strings

text = '\"Python\" tutorial'
print(text)

greeting = 'Hello world'

# extract one character
print(greeting[0])

# extract a substring
print(greeting[0:5])
print(greeting[4:])
print(greeting[0:5])

# multiply a string
print(greeting * 3)

# concat strings
var = '. How are you?'
print(greeting + var)


