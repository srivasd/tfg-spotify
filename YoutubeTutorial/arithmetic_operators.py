
number1 = 10
number2 = 5

# Add +
total = number1 + number2
print('The add of', number1, 'and', number2, 'is', total)

# Sub -
total = number1 - number2
print('The sub of', number1, 'and', number2, 'is', total)

# Mult *
total = number1 * number2
print('The mult of', number1, 'and', number2, 'is', total)

# Div /
total = number1 / number2
print('The div of', number1, 'and', number2, 'is', total)

total = (3 + 3) * 3
print(total)

# Result of a div
total = 10 // 3
print('Result of the div:', total)

# Mod of a div
total = 10 % 3
print('Mod of the div:', total)

# Pow
total = 2 ** 3
print('Pow result:', total)

var = 10
var += 5
print('Var result:', var)
